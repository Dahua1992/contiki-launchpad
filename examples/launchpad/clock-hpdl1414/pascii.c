#include <stdio.h>
#include <stdint.h>

/*---------------------------------------------------------------------------*/
int 
main(int argc, char *argv[])
{
  uint8_t i = 0;
  printf("-----------------------------------------------------------------\n");
  printf("D  0x\n");
  for(i = 33; i <= 126; i += 3) {
    printf("%u %02x\t%c\t\t%u %02x\t%c\t\t%u %02x\t%c\t\t\n", i, i, i, i+1, i+1, i+1, i+2, i+2, i+2);
  }
  printf("-----------------------------------------------------------------\n");
  return 0;
}
